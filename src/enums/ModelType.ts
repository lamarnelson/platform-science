/**
 * ModelType.ts
 *
 * This file contains the ModelType enum, which defines different types of models.
 * It provides identifiers for Driver and Shipment models.
 */

/**
 * ModelType
 *
 * Enum representing different types of models (Driver and Shipment Only).
 */
export enum ModelType {
  /**
   * Driver model type.
   */
  Driver = 'driver',

  /**
   * Shipment model type.
   */
  Shipment = 'shipment',
}
